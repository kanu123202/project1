            <h2> FAQs </h2>
            <div class="tabs">
               <div class="tab">
                  <input type="checkbox" id="chck1">
                  <label class="tab-label" for="chck1">How Long Does It Take To Get A Student Visa?</label>
                  <div class="tab-content">
                     <div class="tab-border-div">
                        
                        <div>
                           The processing time for a student visa is between 2-4 weeks.<br>University admission process will affect the overall processing time for your student visa. Hence, it is always advisable to apply well in advance, ideally 2 months before the intended intake.
                        </div>
                     </div>
                  </div>
               </div>
               <div class="tab">
                  <input type="checkbox" id="chck2">
                  <label class="tab-label" for="chck2">How much does a student visa cost?</label>
                  <div class="tab-content">
                     <div class="tab-border-div">
                        <div>
                           Admission and visa application related fees will vary from university to university depending on their administration costs. However, the cost of a study visa alone will be 150 CAD. The study permit is not a visa. A visitor visa or eTA for Canada will be issued along with the study permit, whichever is required in the specific situation.
                        </div>
                     </div>
                  </div>
               </div>
               <div class="tab">
                  <input type="checkbox" id="chck3">
                  <label class="tab-label" for="chck3">What IELTS Score Is Required For A Canada Student Visa?</label>
                  <div class="tab-content">
                     <div class="tab-border-div">
                        <div>
                           Typically, the Designated Learning Institutions (DLIs) in Canada require at least an overall IELTS Band score of 6, with a minimum of Band 6 in each of the four abilities assessed – Speaking, Listening, Reading, and Writing. However, some Canadian universities and colleges might consider an overall IELTS Band score of 5.5.
                        </div>
                     </div>
                  </div>
               </div>
               <div class="tab">
                  <input type="checkbox" id="chck5">
                  <label class="tab-label" for="chck5">Do I need a PCC/Medicals for a Student Visa?</label>
                  <div class="tab-content">
                     <div class="tab-border-div">
                        <div>
                           PCC is not required for a student visa. However, students must undergo a mandatory medical check immediately after arrival as part of the residence visa application. Applicants will have to compulsorily meet the medical fitness requirements.
                        </div>
                     </div>
                  </div>
               </div>
               <div class="tab">
